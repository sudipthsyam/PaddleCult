						$(document).ready(function () {
    function calculateTotal() {
        var checkinDate = parseDate($('#checkin_date').val());
        var checkoutDate = parseDate($('#checkout_date').val());

        if (isNaN(checkinDate.getTime()) || isNaN(checkoutDate.getTime()) || checkoutDate <= checkinDate) {
            $('#total').val("Invalid Date");
            return;
        }

        var timeDiff = checkoutDate - checkinDate;
        var daysDiff = Math.ceil(timeDiff / (1000 * 3600 * 24));

        var bookingType = $("input[name='booking_type']:checked").val();

        // Hide all dropdowns initially
        $('#num_guests, #num_rooms, #room_type, #dropdown_icon').hide();
        $('#room_type, #num_rooms, #num_guests').prop('required', false);

        if (bookingType === "Beginner Surfing") {
            $('#num_guests').show().prop('required', true);

            if (!$('#num_guests').val()) {
                return;
            }

            var numGuests = parseInt($('#num_guests').val()) || 1;
            var individualPrice = 1500 * numGuests;
            var pricingUnit = "Person";
            var totalAmount = individualPrice * daysDiff;

            $('#rate').val(1500);
        } else if (bookingType === "Resort Stay") {
            $('#num_rooms, #room_type, #dropdown_icon').show().prop('required', true);

            if (!$('#room_type').val() || !$('#num_rooms').val()) {
                return;
            }

            var roomType = $("#room_type").val();
            var roomRatesResortStay = {
                "King Room with Garden View": 7000,
                "Double Room with Garden View": 5000,
                "Standard Double Room": 4000
            };

            if (roomRatesResortStay[roomType]) {
                var individualPrice = roomRatesResortStay[roomType];
                var pricingUnit = "Night";
                var numRooms = parseInt($('#num_rooms').val()) || 1;

                var totalAmount = (individualPrice * numRooms) * daysDiff;
                $('#rate').val(individualPrice);
            } else {
                var individualPrice = 0;
                var pricingUnit = "";
                var totalAmount = 0;
                $('#rate').val("");
            }
        } else if (bookingType === "Surf & Stay") {
            // Show the necessary dropdowns for Surf & Stay
            $('#num_guests, #room_type').show().prop('required', true);

            if (!$('#num_guests').val() || !$('#room_type').val()) {
                $('#total').val("Select a Booking Type");
                $('#rate').val("Select a Booking Type");
                return;
            }

            var roomType = $("#room_type").val();
            var numGuests = parseInt($('#num_guests').val()) || 1;

            if (roomType === "King Room with Garden View" || roomType === "Double Room with Garden View") {
                var basePrice = (roomType === "King Room with Garden View") ? 7000 : 5000;
                var maxGuests = 2;

                if (numGuests > maxGuests) {
                    var additionalRooms = Math.ceil((numGuests - maxGuests) / maxGuests);
                    individualPrice = basePrice + (1500 * numGuests) + (additionalRooms * basePrice);
                } else {
                    individualPrice = basePrice + (1500 * numGuests);
                }
            } else if (roomType === "Standard Double Room") {
                var basePrice = 4000;
